    private void confirmIssueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmIssueActionPerformed
        // TODO add your handling code here:
        Member member = new Member();
        Book book = new Book();
        IssueBook issue = new IssueBook();
        
        String selectedMember = String.valueOf(memberCB.getSelectedItem());
        int memberId = Integer.parseInt(selectedMember.substring(selectedMember.lastIndexOf("/") + 1));
        
        String selectedBook = String.valueOf(bookCB.getSelectedItem());
        int bookId = Integer.parseInt(selectedBook.substring(selectedBook.lastIndexOf("/") + 1));
        
        issue.setMemberId(memberId);
        issue.setBookId(bookId);
        issue.setEdition(editionField.getText());
        issue.setIssueDate(Date.valueOf(issueDateField.getText()));
        issue.setReturnDate(Date.valueOf(returnDateField.getText()));
        
        member.setId(memberId);
        book.setId(bookId);
        boolean success = new IssueBookDao().issueBook(issue, member, book);
        if(success){
            JOptionPane.showMessageDialog(this, "Book issued successfully!");
        }
        else{
            JOptionPane.showMessageDialog(this, "Book cannot be added!");
        }
        
    }//GEN-LAST:event_confirmIssueActionPerformed

