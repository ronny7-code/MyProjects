    private void memberCBItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_memberCBItemStateChanged
        // TODO add your handling code here:
        String id = String.valueOf(memberCB.getSelectedItem());
        int memberId = Integer.parseInt(id.substring(id.lastIndexOf("/")+1));
        
        selectedMember = new MemberDao().getMemberById(memberId);
        String memType = selectedMember.getMembershipType();
        membershipCB.setSelectedItem(memType);
        membershipCB.repaint();
        memberCB.hidePopup();
    }//GEN-LAST:event_memberCBItemStateChanged


