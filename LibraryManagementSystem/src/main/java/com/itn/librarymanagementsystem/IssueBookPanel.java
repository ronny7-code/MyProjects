    private void loadData() {
       ArrayList<Book> books = new BookDao().getAllBooks();
       ArrayList<Member> members = new MemberDao().getAllMembers();
       
       for(Book book:books){
           bookCB.addItem(book.getName()+ "/" + book.getId());
       }
       for(Member member:members){
           memberCB.addItem(member.getFirstName()+ " " + member.getLastName()+ "/" + member.getId());
       }
    }
